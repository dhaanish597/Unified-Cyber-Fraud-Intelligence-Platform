"""Fusion evaluation set generator.

=============================================================================
HONESTY NOTICE — READ BEFORE USING THESE NUMBERS IN A DEMO OR DECK
=============================================================================
This is synthetic data with known labels generated for evaluation purposes.
The fraud base rate is deliberately realistic (0.2%). Every transaction,
including its cyber telemetry overlay (cyber_compromise_in_window) and its
ground-truth is_fraud label, is completely synthetic and generated by this
script. Present it to judges exactly this way.
=============================================================================
"""

import random
import pandas as pd
from pathlib import Path
from api.synthetic_universe.customer_generator import generate_customers_batch
from api.synthetic_universe.transaction_behavior_engine import generate_transaction_universe

def main():
    print("Generating synthetic customers...")
    customers = generate_customers_batch(count=500, seed=42)

    print("Generating 50,000 synthetic transactions (0.2% fraud rate)...")
    # anomaly_pct=0.002 -> 100 anomalies out of 50000
    txns = generate_transaction_universe(
        customers=customers,
        total_txns=50000,
        anomaly_pct=0.002,
        seed=42
    )

    df = pd.DataFrame(txns)

    # In transaction_behavior_engine.py, anomalies are hardcoded to have channel_risk_score == 0.94
    df["is_fraud"] = (df["channel_risk_score"] == 0.94).astype(int)

    # Class balance and feature summary
    fraud_count = df["is_fraud"].sum()
    total_count = len(df)
    print("\n=== Generation Complete ===")
    print(f"Total Transactions: {total_count:,}")
    print(f"Fraud Transactions: {fraud_count:,} ({(fraud_count / total_count) * 100:.2f}% base rate)")
    
    print("\nFeature Summary:")
    print(df.info())
    
    print("\nFraud vs Non-Fraud cyber_compromise_in_window rates:")
    print(df.groupby("is_fraud")["cyber_compromise_in_window"].mean())

    # Save to ml/eval_set.parquet or fallback to .csv
    out_dir = Path(__file__).parent
    out_dir.mkdir(parents=True, exist_ok=True)
    
    parquet_path = out_dir / "eval_set.parquet"
    csv_path = out_dir / "eval_set.csv"
    
    try:
        df.to_parquet(parquet_path, index=False)
        print(f"\nSaved evaluation set to {parquet_path}")
    except Exception as e:
        print(f"\nFailed to save as Parquet: {e}")
        df.to_csv(csv_path, index=False)
        print(f"Saved evaluation set to {csv_path} instead.")

if __name__ == "__main__":
    main()
