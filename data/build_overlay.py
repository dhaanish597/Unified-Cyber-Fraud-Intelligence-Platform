# Step 2: synthetic identities + mule clusters
# =============================================================================


def build_identity_table(accounts: list[str], rng: np.random.Generator) -> pd.DataFrame:
    """One synthetic identity per unique PaySim account id (nameOrig/nameDest)."""
    n = len(accounts)
    lats = rng.uniform(*INDIA_LAT_RANGE, size=n)
    lons = rng.uniform(*INDIA_LON_RANGE, size=n)
    identity = pd.DataFrame({
        "account_id": accounts,
        "user_id": [f"usr_{i:06d}" for i in range(n)],
        "device_id": [random_device_id(rng) for _ in range(n)],
        "ip": [random_ip(rng) for _ in range(n)],
        "home_lat": lats,
        "home_lon": lons,
        "mule_cluster_id": pd.array([None] * n, dtype="object"),
    })
    return identity


def assign_mule_clusters(
    identity: pd.DataFrame,
    preferred_accounts: list[str],
    rng: np.random.Generator,
) -> pd.DataFrame:
    """Group a subset of accounts into device/IP-sharing mule clusters.

    Preferred accounts (fraud beneficiaries) are used first so the ring
    structure is actually visible on the transactions that matter for the demo;
    if there aren't enough of them, random accounts pad out the clusters.
    """
    identity = identity.set_index("account_id")
    needed = N_MULE_CLUSTERS * MULE_CLUSTER_SIZE

    pool = list(dict.fromkeys(preferred_accounts))  # de-dup, keep order
    rng.shuffle(pool)
    if len(pool) < needed:
        remaining = list(set(identity.index) - set(pool))
        rng.shuffle(remaining)
        pool = pool + remaining[: needed - len(pool)]
    pool = pool[:needed]

    for cluster_idx in range(N_MULE_CLUSTERS):
        members = pool[cluster_idx * MULE_CLUSTER_SIZE : (cluster_idx + 1) * MULE_CLUSTER_SIZE]
        if not members:
            continue
        cluster_id = f"mule_{cluster_idx:03d}"
        shared_device = random_device_id(rng)
        shared_ip = random_ip(rng)
        # Anchor the cluster's shared baseline geo to the first member's location.
        anchor_lat = identity.loc[members[0], "home_lat"]
        anchor_lon = identity.loc[members[0], "home_lon"]
        for acc in members:
            identity.loc[acc, "device_id"] = shared_device
            identity.loc[acc, "ip"] = shared_ip
            identity.loc[acc, "home_lat"] = anchor_lat
            identity.loc[acc, "home_lon"] = anchor_lon
            identity.loc[acc, "mule_cluster_id"] = cluster_id

    return identity.reset_index()


