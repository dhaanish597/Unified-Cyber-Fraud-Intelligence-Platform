def write_overlay_report(
    txns: pd.DataFrame,
    events: pd.DataFrame,
    identity: pd.DataFrame,
    selected_frauds: pd.DataFrame,
) -> None:
    n_fraud = int(txns["isFraud"].sum())
    n_cyber_preceded = len(selected_frauds)
    n_mule_clusters = identity["mule_cluster_id"].nunique(dropna=True)
    n_mule_accounts = identity["mule_cluster_id"].notna().sum()
    event_counts = events["event_type"].value_counts()

    lines = [
        "# Fusion Overlay Report",
        "",
        "> Synthetic correlation harness — see the honesty notice at the top of "
        "`data/build_overlay.py`. This is not real joined bank data.",
        "",
        f"- Random seed: `{RANDOM_SEED}`",
        f"- Transactions kept: {len(txns):,} (types: {', '.join(KEPT_TXN_TYPES)})",
        f"- Fraud transactions: {n_fraud:,} ({n_fraud / len(txns) * 100:.3f}% of kept transactions)",
        f"- Fraud transactions with a preceding cyber-compromise event: "
        f"{n_cyber_preceded:,} ({n_cyber_preceded / n_fraud * 100:.1f}% of fraud, "
        f"target was {CYBER_PRECEDED_FRAUD_FRACTION * 100:.0f}%)",
        f"- Fraud transactions with NO preceding cyber signal (silos-only would need to catch these "
        f"on tabular/graph signal alone): {n_fraud - n_cyber_preceded:,}",
        f"- Mule clusters: {n_mule_clusters} clusters, {n_mule_accounts} accounts "
        f"(~{MULE_CLUSTER_SIZE} accounts/cluster sharing one device + IP)",
        f"- Cyber events generated: {len(events):,}",
        "",
        "## Event-type distribution",
        "",
        "| event_type | count |",
        "|---|---|",
    ]
    for event_type, count in event_counts.items():
        lines.append(f"| {event_type} | {count:,} |")

    lines += [
        "",
        "## What this proves and doesn't prove",
        "",
        "- Proves: when a cyber-compromise precedes a fraudulent transfer within the fusion "
        "window, `cyber_compromise_in_window` reliably flags it — this is the signal the fusion "
        "risk engine (Prompt 3) will use, alongside tabular and graph features.",
        "- Does not prove: that this correlation exists in real bank data. It's a controlled "
        "evaluation harness demonstrating fusion uplift *when* the correlation is present.",
        f"- By construction, {CYBER_PRECEDED_FRAUD_FRACTION * 100:.0f}% of frauds have this "
        "precursor and the rest do not — so a model scoring 100% recall via this feature alone "
        "would still miss the silent frauds, same as it should in reality.",
    ]

    (PROCESSED_DIR / "overlay_report.md").write_text("\n".join(lines), encoding="utf-8")
    print(f"-> Wrote {PROCESSED_DIR / 'overlay_report.md'}")


# =============================================================================
# Sanity check — previews the fusion-uplift story without training a real model
# =============================================================================


def sanity_check(txns: pd.DataFrame) -> None:
    """Tiny, dependency-light preview of fusion uplift.

    This is NOT the final metric (that's Prompt 2/3's job with a properly
    trained, held-out-evaluated model at a matched false-positive rate). It
    uses a naive log(amount) percentile as a stand-in "tabular-only" signal to
    show, directionally, that adding the cyber-context feature recovers
    cyber-preceded frauds the naive signal alone misses.
    """
    print("\n=== Sanity check: naive tabular-only vs. tabular+cyber-context ===")
    print("(naive heuristic preview only — see ml/metrics_report.md for the real numbers)")

    cyber_preceded = txns[txns["cyber_compromise_in_window"] & (txns["isFraud"] == 1)]
    silent_fraud = txns[~txns["cyber_compromise_in_window"] & (txns["isFraud"] == 1)]
    print(f"Cyber-preceded frauds: {len(cyber_preceded):,} | Silent frauds: {len(silent_fraud):,}")

    naive_score = np.log1p(txns["amount"])
    flag_threshold = naive_score.quantile(0.99)  # flag top 1% by naive amount score
    tabular_only_flag = naive_score >= flag_threshold
    fused_flag = tabular_only_flag | txns["cyber_compromise_in_window"]

    tabular_recall = tabular_only_flag[cyber_preceded.index].mean() if len(cyber_preceded) else float("nan")
    fused_recall = fused_flag[cyber_preceded.index].mean() if len(cyber_preceded) else float("nan")

    tabular_fp_rate = tabular_only_flag[txns["isFraud"] == 0].mean()
    fused_fp_rate = fused_flag[txns["isFraud"] == 0].mean()

    print(f"\nOn the {len(cyber_preceded):,} cyber-preceded frauds:")
    print(f"  naive tabular-only (top-1%-by-amount) recall : {tabular_recall * 100:.1f}%")
    print(f"  tabular + cyber-context recall                : {fused_recall * 100:.1f}%")
    print(f"  preview uplift                                : "
          f"+{(fused_recall - tabular_recall) * 100:.1f} points")
    print(f"\nFalse-positive rate on non-fraud transactions (not matched, preview only):")
    print(f"  naive tabular-only FP rate : {tabular_fp_rate * 100:.3f}%")
    print(f"  fused FP rate              : {fused_fp_rate * 100:.3f}%")
    print("\nThis previews the story; ml/metrics_report.md (Prompt 2/3) will report the real "
          "PR-AUC/F1/recall-at-fixed-FPR delta from trained models, at a matched operating point.")


