# Step 3-4: timestamps + cyber event log
# =============================================================================


def assign_timestamps(txns: pd.DataFrame, rng: np.random.Generator) -> pd.DataFrame:
    """PaySim's `step` is hours-since-start; add random within-hour jitter so
    transactions get distinct second-level timestamps (needed for a 30-120s
    pre-fraud compromise window to mean anything)."""
    jitter_seconds = rng.integers(0, 3600, size=len(txns))
    txns = txns.copy()
    txns["timestamp"] = BASE_TIMESTAMP + pd.to_timedelta(txns["step"], unit="h") + pd.to_timedelta(
        jitter_seconds, unit="s"
    )
    return txns


def random_points_at_distance_vec(lat, lon, distance_km, rng: np.random.Generator):
    """Vectorized form of random_point_at_distance for bulk login generation."""
    n = len(lat)
    bearing = rng.uniform(0, 2 * np.pi, size=n)
    ang_dist = distance_km / EARTH_RADIUS_KM
    lat1, lon1 = np.radians(lat), np.radians(lon)
    lat2 = np.arcsin(
        np.sin(lat1) * np.cos(ang_dist) + np.cos(lat1) * np.sin(ang_dist) * np.cos(bearing)
    )
    lon2 = lon1 + np.arctan2(
        np.sin(bearing) * np.sin(ang_dist) * np.cos(lat1),
        np.cos(ang_dist) - np.sin(lat1) * np.sin(lat2),
    )
    return np.degrees(lat2), np.degrees(lon2)


def make_normal_login_events(
    identity: pd.DataFrame, txns: pd.DataFrame, rng: np.random.Generator
) -> pd.DataFrame:
    """A handful of ordinary logins per user, scattered across the observation
    window, near the user's home geo/device/IP. Vectorized: identity tables here
    run into the hundreds of thousands of rows, and a Python-level loop over
    every (user, login) pair is the dominant cost of the whole generator."""
    span_start = txns["timestamp"].min()
    span_end = txns["timestamp"].max()
    span_seconds = max(int((span_end - span_start).total_seconds()), 1)

    n_logins = rng.poisson(NORMAL_LOGINS_PER_USER_MEAN, size=len(identity))
    total = int(n_logins.sum())
    if total == 0:
        return pd.DataFrame(columns=[
            "timestamp", "user_id", "device_id", "ip", "event_type",
            "geo_lat", "geo_lon", "km_from_baseline", "severity", "related_txn_id",
        ])

    repeat_idx = np.repeat(np.arange(len(identity)), n_logins)
    home_lat = identity["home_lat"].to_numpy()[repeat_idx]
    home_lon = identity["home_lon"].to_numpy()[repeat_idx]

    offsets = rng.integers(0, span_seconds, size=total)
    dist_km = rng.uniform(*NORMAL_LOGIN_JITTER_KM, size=total)
    lat, lon = random_points_at_distance_vec(home_lat, home_lon, dist_km, rng)

    return pd.DataFrame({
        "timestamp": span_start + pd.to_timedelta(offsets, unit="s"),
        "user_id": identity["user_id"].to_numpy()[repeat_idx],
        "device_id": identity["device_id"].to_numpy()[repeat_idx],
        "ip": identity["ip"].to_numpy()[repeat_idx],
        "event_type": "login_normal",
        "geo_lat": lat,
        "geo_lon": lon,
        "km_from_baseline": np.round(dist_km, 2),
        "severity": "info",
        "related_txn_id": None,
    })


def make_compromise_event(
    ident_row: pd.Series, txn_timestamp: pd.Timestamp, kind: str, rng: np.random.Generator,
    related_txn_id: str | None,
) -> list[dict]:
    """Build the compromise event (or short sequence, for credential stuffing)
    that fires shortly before `txn_timestamp`."""
    lead_s = int(rng.integers(*COMPROMISE_LEAD_TIME_SECONDS))
    event_ts = txn_timestamp - pd.Timedelta(seconds=lead_s)
    home_lat, home_lon = ident_row["home_lat"], ident_row["home_lon"]

    if kind == "impossible_travel_login":
        dist_km = rng.uniform(*IMPOSSIBLE_TRAVEL_KM)
        lat, lon = random_point_at_distance(home_lat, home_lon, dist_km, rng)
        return [{
            "timestamp": event_ts,
            "user_id": ident_row["user_id"],
            "device_id": ident_row["device_id"],
            "ip": random_ip(rng),
            "event_type": "impossible_travel_login",
            "geo_lat": lat,
            "geo_lon": lon,
            "km_from_baseline": round(dist_km, 2),
            "severity": "critical",
            "related_txn_id": related_txn_id,
        }]

    if kind == "new_device_mfa_cookie_reuse":
        dist_km = rng.uniform(*NEW_DEVICE_LOCAL_KM)
        lat, lon = random_point_at_distance(home_lat, home_lon, dist_km, rng)
        return [{
            "timestamp": event_ts,
            "user_id": ident_row["user_id"],
            "device_id": random_device_id(rng),  # a device never seen before for this user
            "ip": ident_row["ip"],
            "event_type": "new_device_mfa_cookie_reuse",
            "geo_lat": lat,
            "geo_lon": lon,
            "km_from_baseline": round(dist_km, 2),
            "severity": "high",
            "related_txn_id": related_txn_id,
        }]

    if kind == "credential_stuffing_success":
        dist_km = rng.uniform(*STUFFING_KM)
        lat, lon = random_point_at_distance(home_lat, home_lon, dist_km, rng)
        attacker_ip = random_ip(rng)
        rows = []
        n_failed = int(rng.integers(2, 5))
        for i in range(n_failed, 0, -1):
            fail_ts = event_ts - pd.Timedelta(seconds=int(rng.integers(20, 90)) * i)
            rows.append({
                "timestamp": fail_ts,
                "user_id": ident_row["user_id"],
                "device_id": random_device_id(rng),
                "ip": attacker_ip,
                "event_type": "login_failed",
                "geo_lat": lat,
                "geo_lon": lon,
                "km_from_baseline": round(dist_km, 2),
                "severity": "medium",
                "related_txn_id": related_txn_id,
            })
        rows.append({
            "timestamp": event_ts,
            "user_id": ident_row["user_id"],
            "device_id": random_device_id(rng),
            "ip": attacker_ip,
            "event_type": "credential_stuffing_success",
            "geo_lat": lat,
            "geo_lon": lon,
            "km_from_baseline": round(dist_km, 2),
            "severity": "critical",
            "related_txn_id": related_txn_id,
        })
        return rows

    raise ValueError(f"Unknown compromise event kind: {kind}")


def choose_cyber_preceded_frauds(txns: pd.DataFrame, rng: np.random.Generator) -> pd.DataFrame:
    fraud_txns = txns[txns["isFraud"] == 1]
    n_selected = int(round(len(fraud_txns) * CYBER_PRECEDED_FRAUD_FRACTION))
    selected = fraud_txns.sample(n=n_selected, random_state=RANDOM_SEED)
    return selected


def generate_cyber_events(
    txns: pd.DataFrame, identity: pd.DataFrame, rng: np.random.Generator
) -> tuple[pd.DataFrame, pd.DataFrame]:
    identity_by_account = identity.set_index("account_id")

    # 1. Ordinary background login traffic for every identity that appears as an
    #    originator (the "logged-in" side of a transaction).
    orig_accounts = txns["nameOrig"].unique()
    orig_identity = identity[identity["account_id"].isin(orig_accounts)]
    normal_events_df = make_normal_login_events(orig_identity, txns, rng)
    rows = []

    # 2. Choose which frauds get a preceding compromise, and inject it.
    selected_frauds = choose_cyber_preceded_frauds(txns, rng)
    kinds = list(COMPROMISE_EVENT_TYPES.keys())
    weights = list(COMPROMISE_EVENT_TYPES.values())

    for _, txn in selected_frauds.iterrows():
        ident_row = identity_by_account.loc[txn["nameOrig"]]
        kind = rng.choice(kinds, p=weights)
        rows.extend(make_compromise_event(ident_row, txn["timestamp"], kind, rng, txn["txn_id"]))

    # 3. Noise: unrelated compromise events on users NOT in the selected slice,
    #    so cyber_compromise_in_window isn't a trivial stand-in for isFraud.
    selected_users = set(selected_frauds["nameOrig"])
    noise_candidates = [a for a in orig_accounts if a not in selected_users]
    n_noise = int(round(len(noise_candidates) * NOISE_COMPROMISE_USER_FRACTION))
    noise_accounts = rng.choice(noise_candidates, size=min(n_noise, len(noise_candidates)), replace=False)

    span_start, span_end = txns["timestamp"].min(), txns["timestamp"].max()
    span_seconds = max(int((span_end - span_start).total_seconds()), 1)
    for account in noise_accounts:
        ident_row = identity_by_account.loc[account]
        offset = rng.integers(0, span_seconds)
        noise_ts = span_start + pd.Timedelta(seconds=int(offset))
        kind = rng.choice(kinds, p=weights)
        rows.extend(make_compromise_event(ident_row, noise_ts, kind, rng, related_txn_id=None))

    events = pd.concat([normal_events_df, pd.DataFrame(rows)], ignore_index=True)
    events = events.sort_values("timestamp").reset_index(drop=True)
    events.insert(0, "event_id", [f"evt_{i:07d}" for i in range(len(events))])
    return events, selected_frauds


# =============================================================================
# Step 5: cyber_compromise_in_window feature
# =============================================================================

COMPROMISE_EVENT_KINDS = set(COMPROMISE_EVENT_TYPES.keys())


def compute_cyber_compromise_in_window(
    txns: pd.DataFrame, events: pd.DataFrame, window_seconds: int = DEFAULT_FUSION_WINDOW_SECONDS
) -> pd.Series:
    """For every transaction, True if a compromise-type event for that
    transaction's originating user fired within `window_seconds` before it.

    Requires txns["user_id"] to already hold the synthetic identity id (not the
    raw PaySim nameOrig) — that's what fused_events.csv's user_id column uses.
    """
    compromise_events = events[events["event_type"].isin(COMPROMISE_EVENT_KINDS)][
        ["user_id", "timestamp"]
    ].rename(columns={"timestamp": "event_timestamp"})

    if compromise_events.empty:
        return pd.Series(False, index=txns.index)

    left = txns[["txn_id", "user_id", "timestamp"]].copy()
    left = left.sort_values("timestamp")
    compromise_events = compromise_events.sort_values("event_timestamp")

    merged = pd.merge_asof(
        left,
        compromise_events,
        left_on="timestamp",
        right_on="event_timestamp",
        by="user_id",
        direction="backward",
        allow_exact_matches=False,
    )
    delta = (merged["timestamp"] - merged["event_timestamp"]).dt.total_seconds()
    in_window = delta.notna() & (delta >= 0) & (delta <= window_seconds)
    in_window.index = merged["txn_id"]
    return in_window.reindex(txns["txn_id"]).fillna(False).to_numpy()


