def find_paysim_csv() -> Path:
    candidates = sorted(RAW_DIR.glob("**/*.csv"))
    paysim_candidates = [
        p for p in candidates
        if "paysim" in str(p).lower() or "ps_2017" in p.name.lower() or "PS_2017" in p.name
    ]
    if not paysim_candidates:
        # Fall back to the biggest CSV in raw/, which is almost always PaySim's.
        if not candidates:
            raise FileNotFoundError(
                f"No CSV files found under {RAW_DIR}. Run `python data/download.py` first."
            )
        paysim_candidates = [max(candidates, key=lambda p: p.stat().st_size)]
    return paysim_candidates[0]


def load_paysim(rng: np.random.Generator) -> pd.DataFrame:
    csv_path = find_paysim_csv()
    print(f"-> Loading PaySim from {csv_path}")
    df = pd.read_csv(csv_path)

    df = df[df["type"].isin(KEPT_TXN_TYPES)].copy()
    print(f"   {len(df):,} rows after filtering to {KEPT_TXN_TYPES}")

    fraud_df = df[df["isFraud"] == 1]
    nonfraud_df = df[df["isFraud"] == 0]
    print(f"   {len(fraud_df):,} fraud rows (always kept), {len(nonfraud_df):,} non-fraud rows")

    budget_nonfraud = max(MAX_TOTAL_TRANSACTIONS - len(fraud_df), 0)
    if len(nonfraud_df) > budget_nonfraud:
        nonfraud_df = nonfraud_df.sample(n=budget_nonfraud, random_state=RANDOM_SEED)
        print(f"   Sampled non-fraud rows down to {len(nonfraud_df):,} to respect "
              f"MAX_TOTAL_TRANSACTIONS={MAX_TOTAL_TRANSACTIONS:,}")

    out = pd.concat([fraud_df, nonfraud_df], ignore_index=True)
    out = out.sort_values("step").reset_index(drop=True)
    out["txn_id"] = [f"txn_{i:07d}" for i in range(len(out))]
    print(f"   Final kept transaction count: {len(out):,} "
          f"({out['isFraud'].sum():,} fraud, {out['isFraud'].mean() * 100:.3f}% fraud rate)")
    return out


