"""Fusion overlay generator — the core innovation of this build.

=============================================================================
HONESTY NOTICE — READ BEFORE USING THESE NUMBERS IN A DEMO OR DECK
=============================================================================
No public dataset links a real user's cyber/login events to their fraud
transactions. That gap IS the market gap this project targets. This script
does NOT join real bank data to real SIEM data — it SYNTHESIZES the link on
top of PaySim as a controlled, fully-reproducible evaluation harness so we
can measure whether a fused engine recovers frauds a tabular-only model
would miss, when a cyber precursor exists. Every "compromise event" here is
generated by this script, not observed. Present it to judges exactly this
way (see Docs/ Judge Q&A #1).
=============================================================================

Pipeline:
  1. Load PaySim from data/raw, filter to TRANSFER + CASH_OUT (where PaySim fraud
     concentrates), sample down to a manageable size while keeping ALL fraud rows.
  2. Assign every account a synthetic identity (user_id, device_id, ip, home geo).
     A subset of destination (beneficiary) accounts are deliberately grouped into
     mule clusters that share a device/IP, to create detectable ring structure.
  3. Generate a per-user cyber event log: mostly normal logins, plus injected
     compromise sequences (impossible-travel login, new-device + MFA-cookie
     reuse, credential-stuffing success) inspired by UNSW-NB15 attack categories.
  4. For a configurable slice of fraud transactions, place a compromise event
     30-120s before the fraud. The rest of the frauds get no preceding cyber
     signal, so fusion uplift is measurable rather than universal by construction.
  5. Compute `cyber_compromise_in_window` for every transaction: True if a
     compromise event for that transaction's user fired within a configurable
     window (default 300s) before it.
  6. Write data/processed/transactions.csv, data/processed/fused_events.csv, and
     data/processed/overlay_report.md.

Fully reproducible: one fixed RANDOM_SEED, all knobs live in the CONFIG block below.

Usage:
    python data/build_overlay.py
"""

from __future__ import annotations

import math
import random
from pathlib import Path

import numpy as np
import pandas as pd

# =============================================================================
# CONFIG — every tunable knob lives here, nowhere else in the file.
# =============================================================================

RANDOM_SEED = 42

RAW_DIR = Path(__file__).parent / "raw"
PROCESSED_DIR = Path(__file__).parent / "processed"

# PaySim fraud lives almost entirely in these two transaction types.
KEPT_TXN_TYPES = ("TRANSFER", "CASH_OUT")

# Cap total kept transactions for a manageable demo-scale dataset. All fraud rows
# are always kept; only non-fraud rows are subsampled down to hit this cap.
MAX_TOTAL_TRANSACTIONS = 400_000

# PaySim's `step` column is hours-since-start (1..743). We map it onto a real
# calendar so events can carry second-level timestamps.
BASE_TIMESTAMP = pd.Timestamp("2026-01-05T00:00:00")

# Mule-ring structure: a subset of beneficiary (destination) accounts are grouped
# into clusters that share one device_id + one IP, biased towards accounts that
# actually receive fraud money so the ring is visible in the demo's threat graph.
MULE_CLUSTER_SIZE = 6
N_MULE_CLUSTERS = 10

# Of the fraud transactions, this fraction get a cyber-compromise event placed
# 30-120s before them. The rest are left as "silent" frauds with no cyber
# precursor -- this is what makes fusion uplift a measurable delta, not 100%.
CYBER_PRECEDED_FRAUD_FRACTION = 0.45
COMPROMISE_LEAD_TIME_SECONDS = (30, 120)

# Default pre-window (seconds) used to compute cyber_compromise_in_window.
# Kept configurable because the risk engine (Prompt 3) may want to tune it.
DEFAULT_FUSION_WINDOW_SECONDS = 300

# Compromise event "flavors", inspired by UNSW-NB15 attack categories, and their
# relative weight when picking which one to inject before a chosen fraud.
COMPROMISE_EVENT_TYPES = {
    "impossible_travel_login": 0.45,
    "new_device_mfa_cookie_reuse": 0.30,
    "credential_stuffing_success": 0.25,
}

# A small fraction of users who are NOT part of the chosen cyber-preceded fraud
# slice still get an unrelated compromise event somewhere in the timeline. This
# is deliberate realism: a SOC generates alerts that never correlate with fraud,
# so cyber_compromise_in_window must not become a trivial proxy for isFraud.
NOISE_COMPROMISE_USER_FRACTION = 0.03

# Normal login volume per user over the observation window (Poisson mean).
NORMAL_LOGINS_PER_USER_MEAN = 4

# Baseline "home" geo is sampled uniformly inside a rough India bounding box
# (this is a bank-of-Maharashtra-flavored demo, not a claim about real user
# locations).
INDIA_LAT_RANGE = (8.0, 35.0)
INDIA_LON_RANGE = (68.0, 97.0)

# Distance bands (km from baseline) used per event flavor.
NORMAL_LOGIN_JITTER_KM = (0.0, 5.0)
IMPOSSIBLE_TRAVEL_KM = (1500.0, 9000.0)
NEW_DEVICE_LOCAL_KM = (0.0, 80.0)
STUFFING_KM = (0.0, 6000.0)

EARTH_RADIUS_KM = 6371.0


