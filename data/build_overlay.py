# Main
# =============================================================================


def main() -> None:
    rng = set_seeds(RANDOM_SEED)
    PROCESSED_DIR.mkdir(parents=True, exist_ok=True)

    txns = load_paysim(rng)
    txns = assign_timestamps(txns, rng)

    all_accounts = pd.unique(pd.concat([txns["nameOrig"], txns["nameDest"]], ignore_index=True))
    print(f"-> Building identity table for {len(all_accounts):,} unique accounts")
    identity = build_identity_table(list(all_accounts), rng)

    fraud_dest_accounts = txns.loc[txns["isFraud"] == 1, "nameDest"].tolist()
    identity = assign_mule_clusters(identity, fraud_dest_accounts, rng)

    identity_by_account = identity.set_index("account_id")
    txns["user_id"] = txns["nameOrig"].map(identity_by_account["user_id"])
    txns["device_id"] = txns["nameOrig"].map(identity_by_account["device_id"])
    txns["ip"] = txns["nameOrig"].map(identity_by_account["ip"])
    txns["dest_user_id"] = txns["nameDest"].map(identity_by_account["user_id"])
    txns["dest_device_id"] = txns["nameDest"].map(identity_by_account["device_id"])
    txns["dest_ip"] = txns["nameDest"].map(identity_by_account["ip"])
    txns["dest_mule_cluster_id"] = txns["nameDest"].map(identity_by_account["mule_cluster_id"])

    print("-> Generating cyber event log")
    events, selected_frauds = generate_cyber_events(txns, identity, rng)

    print("-> Computing cyber_compromise_in_window feature")
    txns["cyber_compromise_in_window"] = compute_cyber_compromise_in_window(
        txns, events, DEFAULT_FUSION_WINDOW_SECONDS
    )

    txn_cols = [
        "txn_id", "step", "timestamp", "type", "amount",
        "nameOrig", "user_id", "device_id", "ip",
        "oldbalanceOrg", "newbalanceOrig",
        "nameDest", "dest_user_id", "dest_device_id", "dest_ip", "dest_mule_cluster_id",
        "oldbalanceDest", "newbalanceDest",
        "isFraud", "isFlaggedFraud", "cyber_compromise_in_window",
    ]
    out_txns = txns[txn_cols].copy()

    event_cols = [
        "event_id", "timestamp", "user_id", "device_id", "ip", "event_type",
        "geo_lat", "geo_lon", "km_from_baseline", "severity", "related_txn_id",
    ]
    out_events = events[event_cols].copy()

    out_txns.to_csv(PROCESSED_DIR / "transactions.csv", index=False)
    print(f"-> Wrote {PROCESSED_DIR / 'transactions.csv'} ({len(out_txns):,} rows)")
    out_events.to_csv(PROCESSED_DIR / "fused_events.csv", index=False)
    print(f"-> Wrote {PROCESSED_DIR / 'fused_events.csv'} ({len(out_events):,} rows)")

    write_overlay_report(out_txns, out_events, identity, selected_frauds)

    sanity_check(out_txns)


if __name__ == "__main__":
    main()
