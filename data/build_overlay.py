# Helpers
# =============================================================================


def set_seeds(seed: int) -> np.random.Generator:
    random.seed(seed)
    np.random.seed(seed)
    return np.random.default_rng(seed)


def haversine_km(lat1, lon1, lat2, lon2) -> float:
    lat1, lon1, lat2, lon2 = map(math.radians, [lat1, lon1, lat2, lon2])
    dlat, dlon = lat2 - lat1, lon2 - lon1
    a = math.sin(dlat / 2) ** 2 + math.cos(lat1) * math.cos(lat2) * math.sin(dlon / 2) ** 2
    return 2 * EARTH_RADIUS_KM * math.asin(math.sqrt(a))


def random_point_at_distance(lat, lon, distance_km, rng) -> tuple[float, float]:
    """Return a lat/lon roughly `distance_km` away from (lat, lon), random bearing."""
    bearing = rng.uniform(0, 2 * math.pi)
    ang_dist = distance_km / EARTH_RADIUS_KM
    lat1, lon1 = math.radians(lat), math.radians(lon)
    lat2 = math.asin(
        math.sin(lat1) * math.cos(ang_dist) + math.cos(lat1) * math.sin(ang_dist) * math.cos(bearing)
    )
    lon2 = lon1 + math.atan2(
        math.sin(bearing) * math.sin(ang_dist) * math.cos(lat1),
        math.cos(ang_dist) - math.sin(lat1) * math.sin(lat2),
    )
    return math.degrees(lat2), math.degrees(lon2)


def random_ip(rng) -> str:
    return ".".join(str(int(rng.integers(1, 255))) for _ in range(4))


def random_device_id(rng) -> str:
    return "dev_" + "".join(rng.choice(list("0123456789abcdef")) for _ in range(12))


